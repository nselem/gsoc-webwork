#_____ Given an atom from 1BNA (DNA structure) return their PDB data
sub DNAcoord() {
	my $element=$_[0];
	my $ref=$DNA{$element};
	my @Coef=@$ref;
	return(@Coef);
}

#_____ Given an atom from 2VEP (protein structure) return their PDB data
sub PROTcoord() {
	my $element=$_[0];
	my $ref=$_2VEP{$element};
	my @Coef=@$ref;
	return(@Coef);
}

#___________________________________________________
sub atom() {
	my $rand_atom=(keys %BCoef)[random keys %BCoef];
	return $rand_atom;
}

#____________________________________________
sub Bcoef() {
	my $element=$_[0];
	my $ref=$BCoef{$element};
	my @Coef=@$ref;
	return(@Coef);
}

